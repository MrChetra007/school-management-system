union all
select 'student_sick_days',  count(*) from student_sick_days
union all
select 'student_checkups',   count(*) from student_checkups
union all
select 'attendances',        count(*) from attendances
union all
select 'scores',             count(*) from scores
union all
select 'teacher_attendances', count(*) from teacher_attendances
union all
select 'school_holidays',    count(*) from school_holidays
union all
select 'books',              count(*) from books
union all
select 'book_borrows',       count(*) from book_borrows
union all
select 'budget_transactions', count(*) from budget_transactions
union all
